
package trabalho01;

public class Cardapio {
    
    private String nome;
    private int preco;

    public Cardapio() {
    }

    public Cardapio(String nome, int preco) {
        this.nome = nome;
        this.preco = preco;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getPreco() {
        return preco;
    }

    public void setPreco(int preco) {
        this.preco = preco;
    }

   
    
    
    
}
